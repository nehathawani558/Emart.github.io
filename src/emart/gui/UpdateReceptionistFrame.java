    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       if(!validateInputs()){
           return;
       }
       try{
           String id=jcId.getSelectedItem().toString();
           boolean res=ReceptionistDAO.updatePassword(id, password);
           if(res==true)
           {JOptionPane.showMessageDialog(null,"Password changed successfully","Success!",JOptionPane.INFORMATION_MESSAGE);
           clearText();
           jcId.setSelectedIndex(0);
           return;
           }
           JOptionPane.showMessageDialog(null,"Password not changed","ERROR!",JOptionPane.ERROR_MESSAGE);
           clearText();
       }
       catch(SQLException e){
              JOptionPane.showMessageDialog(null,"DB Error","ERROR!",JOptionPane.ERROR_MESSAGE);
              e.printStackTrace();
              }
    }//GEN-LAST:event_btnUpdateActionPerformed

