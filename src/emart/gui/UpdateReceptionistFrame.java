    private void loadReceptionistDetails() {
       try{
           receptionist=ReceptionistDAO.getAllReceptionistId();
           if(receptionist.isEmpty()){
               JOptionPane.showMessageDialog(null,"No receptionist present","EMPTY!",JOptionPane.INFORMATION_MESSAGE);
               btnUpdate.setEnabled(false);
               return;
           }
           btnUpdate.setEnabled(true);
           Set<String> keys=receptionist.keySet();
           Iterator<String> it=keys.iterator();
           while(it.hasNext()){
               jcId.addItem(it.next());
           }
       }
       catch(SQLException e){
            JOptionPane.showMessageDialog(null,"DB Error","ERROR!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
       }
    }
    private boolean validateInputs(){
        char[] pwd=txtPwd.getPassword();
        char[] Npwd=txtNpwd.getPassword();
        if(pwd.length<4 || Npwd.length<4){
        JOptionPane.showMessageDialog(null,"Password must have atleast four character","ERROR!",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        password=String.valueOf(pwd);
        String Npsd=String.valueOf(Npwd);
        if(!password.equals(Npsd)){
            JOptionPane.showMessageDialog(null,"Password not matched","ERROR!",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

     private void clearText() {
        txtPwd.setText("");
        txtNpwd.setText("");
        jcjob.setSelectedIndex(0);
    }
