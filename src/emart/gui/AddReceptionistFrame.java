    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       int res=validateInputs();
       if(res==-1){JOptionPane.showMessageDialog(null,"Please enter UserId","Incomplete data!",JOptionPane.ERROR_MESSAGE);
        return;}
       else if(res==0){
           JOptionPane.showMessageDialog(null,"Password must be atleast four characters long","PASSWORD TOO SHORT" ,JOptionPane.ERROR_MESSAGE);
           return ;}
       char[] pwd=txtPsd.getPassword();
       char[] rpwd=txtNpsd.getPassword();
       String a=new String(pwd);
       String b=new String(rpwd);
       if(!isPasswordMatching(a,b)){
       JOptionPane.showMessageDialog(null,"Password does not match","PASSWORD Error" ,JOptionPane.ERROR_MESSAGE);
           return ;}
       try{
           String empId=jcId.getSelectedItem().toString();
           String userId=txtUserId.getText().trim();
           UserPojo u=new UserPojo();
           u.setEmpid(empId);
           u.setUserid(userId);
           u.setUsername(txtname.getText());
           u.setUsertype("Receptionist");
           u.setPassword(a);
           boolean status=ReceptionistDAO.addReceptionist(u);
           System.out.println(status);
           if(status){
                JOptionPane.showMessageDialog(null,"Record added successfully","Success" ,JOptionPane.INFORMATION_MESSAGE);
                clearText();
                loadReceptionistDetails();
                return ;
           }
           JOptionPane.showMessageDialog(null,"Record could not be added","Error" ,JOptionPane.ERROR_MESSAGE);
       }
       catch(SQLException e){
           JOptionPane.showMessageDialog(null,"DB Error","ERROR!",JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
       }
    }//GEN-LAST:event_btnAddActionPerformed

