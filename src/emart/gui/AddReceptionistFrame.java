    private void loadReceptionistDetails() {
        
        try{
            receptionist=ReceptionistDAO.getNonRegisteredReceptionist();
            if(receptionist.isEmpty())
            {JOptionPane.showMessageDialog(null,"No unregistered users present","Information", JOptionPane.INFORMATION_MESSAGE);
                jcId.removeAllItems();
            btnAdd.setEnabled(false);return;
            }
            btnAdd.setEnabled(true);
            Set keys=receptionist.keySet();
            Iterator<String> it=keys.iterator();
            jcId.removeAllItems();
            while(it.hasNext()){
                jcId.addItem(it.next());
              }
        }
            catch(SQLException e){
              JOptionPane.showMessageDialog(null,"DB Error","ERROR!",JOptionPane.ERROR_MESSAGE);
              e.printStackTrace();
              }
        
    }
private boolean isPasswordMatching(String a,String b){
return a.equals(b);
}
    private int validateInputs() {
        if(txtUserId.getText().isEmpty())
            return -1;
        else if(txtPsd.getPassword().length<4 || txtNpsd.getPassword().length<4)return 0;
        else 
            return 1;
    }

    private void clearText() {
       txtPsd.setText("");
       txtNpsd.setText("");
       txtname.setText("");
       txtUserId.setText("");
    }
